// ---------- Maaltijden ----------

type Row = [slug: string, grams: number, role: IngredientRole]

function meal(
  code: string,
  name: string,
  description: string,
  eligibleSlots: MealSlot[],
  temperature: Temperature,
  portability: Portability,
  digestionSpeed: DigestionSpeed,
  caseinDominant: boolean,
  prepMinutes: number,
  family: string,
  rationaleShort: string,
  rationale: string,
  rows: Row[],
): Meal {
  return {
    id: code,
    code,
    name,
    description,
    eligibleSlots,
    temperature,
    portability,
    digestionSpeed,
    caseinDominant,
    prepMinutes,
    family,
    rationale,
    rationaleShort,
    ingredients: rows.map(([ingredientId, grams, role]) => ({ ingredientId, grams, role })),
  }
}

