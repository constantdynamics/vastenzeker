interface IngredientExtras {
  pieceGrams?: number
  nutType?: string
  isPeanutButter?: boolean
}

function ing(
  slug: string,
  name: string,
  kcal100: number,
  protein100: number,
  carb100: number,
  fat100: number,
  fiber100: number,
  category: IngredientCategory,
  extras: IngredientExtras = {},
): Ingredient {
  return {
    id: slug, // in de app vervangt Supabase dit door het numerieke id
    slug,
    name,
    kcal100,
    protein100,
    carb100,
    fat100,
    fiber100,
    category,
    isNut: extras.nutType != null,
    nutType: extras.nutType ?? null,
    pieceGrams: extras.pieceGrams ?? null,
    isPeanutButter: extras.isPeanutButter ?? false,
    rationale: INGREDIENT_RATIONALES[slug] ?? EXTRA_RATIONALES[slug] ?? null,
    source: 'seed',
    externalId: null,
  }
}

